CREATE OR REPLACE PROCEDURE SP_PLACE_ORDER(
    p_user_id IN NUMBER,
    p_product_id IN NUMBER,
    p_quantity IN NUMBER
)
AS
    v_price NUMBER;
BEGIN

    SELECT PRICE
    INTO v_price
    FROM PRODUCTS
    WHERE PRODUCT_ID = p_product_id;

    INSERT INTO ORDERS
    (
        ORDER_ID,
        PRODUCT_ID,
        USER_ID,
        QUANTITY,
        PRICE,
        STATUS,
        CREATED_AT
    )
    VALUES
    (
        ORDERS_SEQ.NEXTVAL,
        p_product_id,
        p_user_id,
        p_quantity,
        v_price,
        'PLACED',
        SYSTIMESTAMP
    );

    SP_UPDATE_STOCK(
        p_product_id,
        p_quantity
    );

    COMMIT;
END;
