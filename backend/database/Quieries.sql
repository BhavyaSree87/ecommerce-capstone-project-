CREATE OR REPLACE VIEW VW_TOP_SELLING_PRODUCTS AS
SELECT
    p.PRODUCT_ID,
    p.PRODUCT_NAME,
    SUM(o.QUANTITY) AS TOTAL_SOLD
FROM PRODUCTS p
JOIN ORDERS o
ON p.PRODUCT_ID = o.PRODUCT_ID
GROUP BY p.PRODUCT_ID, p.PRODUCT_NAME;


CREATE OR REPLACE VIEW VW_MONTHLY_REVENUE AS
SELECT
    TO_CHAR(TRUNC(CREATED_AT,'MM'),'YYYY-MM') AS MONTH,
    SUM(PRICE * QUANTITY) AS REVENUE
FROM ORDERS
GROUP BY TRUNC(CREATED_AT,'MM');

CREATE OR REPLACE PROCEDURE SP_UPDATE_STOCK(
    p_product_id IN NUMBER,
    p_quantity IN NUMBER
)
AS
    v_stock NUMBER;
BEGIN
    SELECT STOCK
    INTO v_stock
    FROM PRODUCTS
    WHERE PRODUCT_ID = p_product_id;

    IF v_stock < p_quantity THEN
        RAISE_APPLICATION_ERROR(-20001,'Insufficient Stock');
    END IF;

    UPDATE PRODUCTS
    SET STOCK = STOCK - p_quantity
    WHERE PRODUCT_ID = p_product_id;

    COMMIT;
END;
